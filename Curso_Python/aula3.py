"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(123)

# Usando aspas simples para criar strings
print('Alan Turing')  
print(1, 'Isaac "Newton"')  

# Usando aspas duplas para criar strings
print("Marie Curie")  
print(2, "Albert 'Einstein'")  

# Escape com a barra invertida (\) para incluir caracteres especiais
print("Grace \"Hopper\"")  

# Raw string (r) ignora caracteres especiais de escape
print(r"John \"Von Neumann\"")  