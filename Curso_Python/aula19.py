"""
Operadores de comparação (relacionais)

Operador    Significado         Exemplo (True)
-----------------------------------------------
>           Maior               5 > 3
>=          Maior ou igual      4 >= 4
<           Menor               2 < 8
<=          Menor ou igual      6 <= 6
==          Igual               'x' == 'x'
!=          Diferente           'y' != 'z'
"""

# Definição das comparações
maior = 5 > 3
maior_ou_igual = 4 >= 4
menor = 2 < 8
menor_ou_igual = 6 <= 6
igual = 'x' == 'x'
diferente = 'y' != 'z'

# Exibindo os resultados
print(f"Maior: {maior}")
print(f"Maior ou igual: {maior_ou_igual}")
print(f"Menor: {menor}")
print(f"Menor ou igual: {menor_ou_igual}")
print(f"Igual: {igual}")
print(f"Diferente: {diferente}")